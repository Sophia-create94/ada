// Scans public/assets/mood/<folder>/<style>__<city>...jpg and writes a manifest
// (src/data/moodImages.ts) mapping each mood id -> the curated image URLs shown
// when that mood tile is clicked. Re-run after adding/renaming mood images:
//   node scripts/build-mood-images.mjs
import fs from 'fs'
import path from 'path'

const ROOT = 'public/assets/mood'
// folder name -> mood id used in the app (the 'nature' mood lives in /wild)
const FOLDER_TO_MOOD = {
  'sun-soaked': 'sun-soaked',
  buzzing: 'buzzing',
  wild: 'nature',
  storied: 'storied',
  wintry: 'wintry',
}

const out = {}
for (const [folder, mood] of Object.entries(FOLDER_TO_MOOD)) {
  const dir = path.join(ROOT, folder)
  if (!fs.existsSync(dir)) { out[mood] = []; continue }
  out[mood] = fs
    .readdirSync(dir)
    .filter((f) => /__.*\.jpg$/i.test(f))
    .sort()
    .map((f) => `/assets/mood/${folder}/${f}`)
}

const body =
  '// AUTO-GENERATED by scripts/build-mood-images.mjs — do not edit by hand.\n' +
  '// Curated images shown when a mood tile is clicked, keyed by mood id.\n' +
  'export const MOOD_IMAGES: Record<string, string[]> = ' +
  JSON.stringify(out, null, 2) +
  '\n'

fs.writeFileSync('src/data/moodImages.ts', body)
const counts = Object.entries(out).map(([m, a]) => `${m}:${a.length}`).join('  ')
console.log('wrote src/data/moodImages.ts —', counts)
